def _gen_text_classification_dataset(data_contract: dict, output_dir: str) -> list:
    """Generate Dataset + DataLoader for JSONL text classification."""
    input_spec = data_contract.get("input_spec", {})
    output_spec = data_contract.get("output_spec", {})
    splits = data_contract.get("splits", {})

    text_col = input_spec.get("text_column", "text")
    label_col = output_spec.get("target_column", "label")
    max_seq_length = input_spec.get("max_seq_length", 512)
    num_classes = output_spec.get("num_classes", 2)
    label_map = output_spec.get("label_map", {})

    split_config = {
        "train": splits.get("train", "data/train.jsonl"),
        "val": splits.get("val", "data/val.jsonl"),
        "test": splits.get("test", "data/test.jsonl"),
    }

    # Build a simple tokenizer wrapper
    label_map_code = ""
    if label_map:
        label_map_code = f"LABEL_MAP = {json.dumps(label_map, indent=4)}\n"
    else:
        label_map_code = "# LABEL_MAP not provided; labels are assumed to be integer-encoded\n"

    # ---- transforms.py ----
    transforms_code = f'''"""Tokenization transforms for text classification.

Requires a pretrained tokenizer. Install with: pip install transformers
"""


class TextTokenizer:
    """Wraps a HuggingFace tokenizer for Dataset-compatible transforms."""

    def __init__(self, tokenizer_name: str = "bert-base-uncased", max_length: int = {max_seq_length}):
        from transformers import AutoTokenizer
        self.tokenizer = AutoTokenizer.from_pretrained(tokenizer_name)
        self.max_length = max_length

    def __call__(self, text: str):
        encoded = self.tokenizer(
            text,
            max_length=self.max_length,
            padding="max_length",
            truncation=True,
            return_tensors="pt",
        )
        return {{
            "input_ids": encoded["input_ids"].squeeze(0),
            "attention_mask": encoded["attention_mask"].squeeze(0),
        }}
'''

    # ---- dataset.py ----
    dataset_code = _header("dataset") + f'''
import json


TEXT_COLUMN = "{text_col}"
LABEL_COLUMN = "{label_col}"
{label_map_code}

class TextClassificationDataset(Dataset):
    """Dataset for JSONL text classification files.

    Each line is a JSON object with a text field and a label field.
    """

    def __init__(self, jsonl_path: str, transform=None):
        self.transform = transform
        self.texts = []
        self.labels = []

        with open(jsonl_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                obj = json.loads(line)
                self.texts.append(obj[TEXT_COLUMN])
                label = obj[LABEL_COLUMN]
                if LABEL_MAP:
                    label = LABEL_MAP.get(label, label)
                self.labels.append(int(label))

        self.labels = torch.tensor(self.labels, dtype=torch.long)

    def __len__(self):
        return len(self.texts)

    def __getitem__(self, idx):
        text = self.texts[idx]
        label = self.labels[idx]
        if self.transform is not None:
            tokens = self.transform(text)
            return tokens, label
        return text, label
'''

    # ---- dataloader.py ----
    dataloader_code = _header("dataloader") + f'''
from torch.utils.data import DataLoader
from dataset import TextClassificationDataset
from transforms import TextTokenizer


SPLITS = {json.dumps(split_config, indent=4)}
MAX_SEQ_LENGTH = {max_seq_length}
NUM_CLASSES = {num_classes}


def build_dataloader(split: str = "train", batch_size: int = 16, num_workers: int = 0,
                     tokenizer_name: str = "bert-base-uncased"):
    """Build a DataLoader for text classification.

    Args:
        split: One of 'train', 'val', 'test'.
        batch_size: Batch size.
        num_workers: Number of dataloader workers.
        tokenizer_name: HuggingFace tokenizer name.

    Returns:
        DataLoader yielding (token_dict, labels) tuples.
    """
    if split not in SPLITS:
        raise ValueError(f"Unknown split '{{split}}'. Available: {{list(SPLITS.keys())}}")

    tokenizer = TextTokenizer(tokenizer_name=tokenizer_name, max_length=MAX_SEQ_LENGTH)
    dataset = TextClassificationDataset(SPLITS[split], transform=tokenizer)
    shuffle = (split == "train")
    return DataLoader(dataset, batch_size=batch_size, shuffle=shuffle, num_workers=num_workers)
'''

    return [
        ("dataset.py", dataset_code),
        ("transforms.py", transforms_code),
        ("dataloader.py", dataloader_code),
    ]


