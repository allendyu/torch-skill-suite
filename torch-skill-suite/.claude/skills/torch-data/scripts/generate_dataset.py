def _header(module_name: str) -> str:
    return f'''"""Auto-generated {module_name} for PyTorch data pipeline.

Generated by torch-data/generate_dataset.py.
Review and adjust paths, transforms, and split policy before training.
"""

import json
from pathlib import Path

import torch
from torch.utils.data import Dataset, DataLoader
'''


# ---------------------------------------------------------------------------
# Image Classification (ImageFolder)
# ---------------------------------------------------------------------------

def _gen_image_classification_dataset(data_contract: dict, output_dir: str) -> list:
    """Generate Dataset + transforms + DataLoader for ImageFolder classification."""
    input_spec = data_contract.get("input_spec", {})
    output_spec = data_contract.get("output_spec", {})
    splits = data_contract.get("splits", {})
    preprocessing = data_contract.get("preprocessing", [])

    shape = input_spec.get("shape", [3, 224, 224])
    num_classes = output_spec.get("num_classes", 10)
    label_map = output_spec.get("label_map", {})

    h, w = shape[1] if len(shape) >= 2 else 224, shape[2] if len(shape) >= 3 else 224

    # ---- transforms.py ----
    normalize_block = ""
    if preprocessing:
        for step in preprocessing:
            if isinstance(step, dict) and step.get("name") == "normalize":
                params = step.get("params", {})
                mean = params.get("mean", [0.485, 0.456, 0.406])
                std = params.get("std", [0.229, 0.224, 0.225])
                normalize_block = f"""
    transforms.Normalize(mean={mean}, std={std}),"""
                break
    if not normalize_block:
        normalize_block = """
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),"""

    transforms_code = f'''"""Transforms for image classification."""

from torchvision import transforms


def build_train_transforms(height: int = {h}, width: int = {w}):
    """Training transforms with augmentation."""
    return transforms.Compose([
        transforms.Resize((height, width)),
        transforms.RandomHorizontalFlip(),
        transforms.ColorJitter(brightness=0.1, contrast=0.1),
        transforms.ToTensor(),{normalize_block}
    ])


def build_val_transforms(height: int = {h}, width: int = {w}):
    """Validation transforms without augmentation."""
    return transforms.Compose([
        transforms.Resize((height, width)),
        transforms.ToTensor(),{normalize_block}
    ])
'''

    # ---- dataset.py ----
    if label_map:
        label_map_code = f"LABEL_MAP = {json.dumps(label_map, indent=4)}\n"
    else:
        label_map_code = "# LABEL_MAP not provided; class indices are derived from folder sort order\n"

    dataset_code = _header("dataset") + f'''
from torchvision.datasets import ImageFolder

{label_map_code}

def create_imagefolder_dataset(root: str, transform=None):
    """Create an ImageFolder dataset from a class-folder structured directory.

    Expected layout:
        root/
        ├── class_a/
        │   ├── img1.jpg
        │   └── img2.jpg
        └── class_b/
            └── img3.jpg

    Args:
        root: Path to the root directory containing class subdirectories.
        transform: torchvision transforms pipeline.

    Returns:
        ImageFolder dataset instance.
    """
    return ImageFolder(root=root, transform=transform)
'''

    # ---- dataloader.py ----
    split_config = {
        "train": splits.get("train", "data/train"),
        "val": splits.get("val", "data/val"),
        "test": splits.get("test", "data/test"),
    }

    dataloader_code = _header("dataloader") + f'''
from torch.utils.data import DataLoader
from dataset import create_imagefolder_dataset
from transforms import build_train_transforms, build_val_transforms


SPLITS = {json.dumps(split_config, indent=4)}
NUM_CLASSES = {num_classes}
INPUT_SHAPE = {shape}


def build_dataloader(split: str = "train", batch_size: int = 32, num_workers: int = 4):
    """Build a DataLoader for the specified split.

    Args:
        split: One of 'train', 'val', 'test'.
        batch_size: Batch size.
        num_workers: Number of dataloader workers.

    Returns:
        DataLoader for the requested split.
    """
    if split not in SPLITS:
        raise ValueError(f"Unknown split '{{split}}'. Available: {{list(SPLITS.keys())}}")

    is_train = (split == "train")
    transform = build_train_transforms() if is_train else build_val_transforms()
    dataset = create_imagefolder_dataset(SPLITS[split], transform=transform)
    return DataLoader(dataset, batch_size=batch_size, shuffle=is_train, num_workers=num_workers)
'''

    return [
        ("dataset.py", dataset_code),
        ("transforms.py", transforms_code),
        ("dataloader.py", dataloader_code),
    ]


# ---------------------------------------------------------------------------
# Tabular (CSV / TSV)
# ---------------------------------------------------------------------------

def _gen_tabular_dataset(data_contract: dict, output_dir: str) -> list:
    """Generate Dataset + transforms + DataLoader for CSV/TSV tabular data."""
    input_spec = data_contract.get("input_spec", {})
    output_spec = data_contract.get("output_spec", {})
    splits = data_contract.get("splits", {})
    task_type = data_contract.get("task_type", "classification")
    is_regression = task_type == "regression"

    num_features = input_spec.get("shape", [10])[0] if input_spec.get("shape") else 10
    target_col = output_spec.get("target_column", "target")
    feature_cols = input_spec.get("feature_columns", [f"feature_{{i}}" for i in range(num_features)])
    id_col = input_spec.get("id_column")

    split_config = {
        "train": splits.get("train", "data/train.csv"),
        "val": splits.get("val", "data/val.csv"),
        "test": splits.get("test", "data/test.csv"),
    }

    # ---- transforms.py ----
    transforms_code = '''"""Preprocessing transforms for tabular data.

StandardScaler normalizes features to zero mean and unit variance.
Fit on the training set, then apply to val/test.
"""

import torch


class StandardScaler:
    """Standardize features by removing the mean and scaling to unit variance."""

    def __init__(self):
        self.mean = None
        self.std = None

    def fit(self, data: torch.Tensor):
        self.mean = data.mean(dim=0)
        self.std = data.std(dim=0)
        self.std[self.std == 0] = 1.0  # avoid division by zero

    def transform(self, data: torch.Tensor) -> torch.Tensor:
        if self.mean is None:
            raise RuntimeError("StandardScaler must be fit before transform")
        return (data - self.mean) / self.std

    def fit_transform(self, data: torch.Tensor) -> torch.Tensor:
        self.fit(data)
        return self.transform(data)
'''

    # ---- dataset.py ----
    target_dtype = "torch.float32" if is_regression else "torch.long"
    id_col_line = f'        "id_column": {json.dumps(id_col)},' if id_col else ""

    dataset_code = _header("dataset") + f'''
import csv
import torch


class TabularDataset(Dataset):
    """Dataset for CSV/TSV tabular data.

    Reads a CSV file with feature columns and a target column.
    """

    FEATURE_COLUMNS = {json.dumps(feature_cols)}
    TARGET_COLUMN = "{target_col}"
{id_col_line}
    TASK_TYPE = "{task_type}"

    def __init__(self, csv_path: str, transform=None):
        self.transform = transform
        self.features = []
        self.targets = []

        delimiter = "\\t" if csv_path.endswith(".tsv") else ","
        with open(csv_path, "r", encoding="utf-8") as f:
            reader = csv.DictReader(f, delimiter=delimiter)
            for row in reader:
                feat_vals = [float(row[col]) for col in self.FEATURE_COLUMNS]
                self.features.append(feat_vals)
                if self.TASK_TYPE == "regression":
                    self.targets.append(float(row[self.TARGET_COLUMN]))
                else:
                    self.targets.append(int(row[self.TARGET_COLUMN]))

        self.features = torch.tensor(self.features, dtype=torch.float32)
        self.targets = torch.tensor(self.targets, dtype={target_dtype})

    def __len__(self):
        return len(self.features)

    def __getitem__(self, idx):
        x = self.features[idx]
        y = self.targets[idx]
        if self.transform is not None:
            x = self.transform(x)
        return x, y
'''

    # ---- dataloader.py ----
    dataloader_code = _header("dataloader") + f'''
from torch.utils.data import DataLoader
from dataset import TabularDataset
from transforms import StandardScaler


SPLITS = {json.dumps(split_config, indent=4)}
NUM_FEATURES = {num_features}
TASK_TYPE = "{task_type}"

_scaler = None


def _get_scaler():
    global _scaler
    if _scaler is None:
        _scaler = StandardScaler()
    return _scaler


def build_dataloader(split: str = "train", batch_size: int = 64, num_workers: int = 0,
                     fit_scaler: bool = False):
    """Build a DataLoader for tabular data.

    Args:
        split: One of 'train', 'val', 'test'.
        batch_size: Batch size.
        num_workers: Number of dataloader workers.
        fit_scaler: Fit the StandardScaler on the training set before returning.

    Returns:
        DataLoader for the requested split.
    """
    if split not in SPLITS:
        raise ValueError(f"Unknown split '{{split}}'. Available: {{list(SPLITS.keys())}}")

    dataset = TabularDataset(SPLITS[split])
    if fit_scaler and split == "train":
        scaler = _get_scaler()
        dataset.features = scaler.fit_transform(dataset.features)
    elif _scaler is not None:
        dataset.features = _get_scaler().transform(dataset.features)

    shuffle = (split == "train")
    return DataLoader(dataset, batch_size=batch_size, shuffle=shuffle, num_workers=num_workers)
'''

    return [
        ("dataset.py", dataset_code),
        ("transforms.py", transforms_code),
        ("dataloader.py", dataloader_code),
    ]


