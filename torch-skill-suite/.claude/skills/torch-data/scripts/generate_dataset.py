def _gen_image_segmentation_dataset(data_contract: dict, output_dir: str) -> list:
    """Generate Dataset + transforms + DataLoader for image segmentation."""
    input_spec = data_contract.get("input_spec", {})
    output_spec = data_contract.get("output_spec", {})
    splits = data_contract.get("splits", {})

    shape = input_spec.get("shape", [3, 224, 224])
    num_classes = output_spec.get("num_classes", 21)

    h, w = shape[1] if len(shape) >= 2 else 224, shape[2] if len(shape) >= 3 else 224

    split_config = {
        "train": splits.get("train", "data/train"),
        "val": splits.get("val", "data/val"),
        "test": splits.get("test", "data/test"),
    }

    # ---- transforms.py ----
    transforms_code = f'''"""Transforms for image segmentation."""

from torchvision import transforms


def build_train_transforms(height: int = {h}, width: int = {w}):
    """Training transforms with joint augmentation for image and mask."""
    return transforms.Compose([
        transforms.Resize((height, width)),
        transforms.RandomHorizontalFlip(),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])


def build_val_transforms(height: int = {h}, width: int = {w}):
    """Validation transforms."""
    return transforms.Compose([
        transforms.Resize((height, width)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])


def build_mask_transform(height: int = {h}, width: int = {w}):
    """Transform for mask — resize only, no normalization."""
    return transforms.Compose([
        transforms.Resize((height, width), interpolation=transforms.InterpolationMode.NEAREST),
        transforms.PILToTensor(),
    ])
'''

    # ---- dataset.py ----
    dataset_code = _header("dataset") + f'''
from PIL import Image


NUM_CLASSES = {num_classes}
IMAGE_EXTS = {{".jpg", ".jpeg", ".png", ".bmp", ".tiff"}}
MASK_EXTS = {{".png", ".gif", ".tif", ".tiff"}}


def _find_pairs(image_dir: str, mask_dir: str):
    """Find (image, mask) pairs by matching relative stems."""
    image_dir = Path(image_dir)
    mask_dir = Path(mask_dir)

    images = {{}}
    for p in image_dir.rglob("*"):
        if p.is_file() and p.suffix.lower() in IMAGE_EXTS:
            rel = p.relative_to(image_dir)
            stem = str(rel.with_suffix(""))
            images[stem] = p

    masks = {{}}
    for p in mask_dir.rglob("*"):
        if p.is_file() and p.suffix.lower() in MASK_EXTS:
            rel = p.relative_to(mask_dir)
            stem = str(rel.with_suffix(""))
            masks[stem] = p

    common = sorted(set(images) & set(masks))
    return [(images[s], masks[s]) for s in common]


class SegmentationDataset(Dataset):
    """Dataset for image/mask pairs (semantic segmentation).

    Expected layout:
        root/
        ├── images/
        │   ├── sample1.png
        │   └── sample2.png
        └── masks/
            ├── sample1.png
            └── sample2.png
    """

    def __init__(self, root: str, image_transform=None, mask_transform=None):
        self.image_transform = image_transform
        self.mask_transform = mask_transform
        image_dir = str(Path(root) / "images")
        mask_dir = str(Path(root) / "masks")
        self.pairs = _find_pairs(image_dir, mask_dir)

    def __len__(self):
        return len(self.pairs)

    def __getitem__(self, idx):
        image_path, mask_path = self.pairs[idx]
        image = Image.open(image_path).convert("RGB")
        mask = Image.open(mask_path)

        if self.image_transform:
            image = self.image_transform(image)
        if self.mask_transform:
            mask = self.mask_transform(mask)
        mask = mask.squeeze(0).long()  # [1, H, W] -> [H, W]
        return image, mask
'''

    # ---- dataloader.py ----
    dataloader_code = _header("dataloader") + f'''
from torch.utils.data import DataLoader
from dataset import SegmentationDataset
from transforms import build_train_transforms, build_val_transforms, build_mask_transform


SPLITS = {json.dumps(split_config, indent=4)}
NUM_CLASSES = {num_classes}
INPUT_SHAPE = {shape}


def build_dataloader(split: str = "train", batch_size: int = 8, num_workers: int = 4):
    """Build a DataLoader for image segmentation.

    Args:
        split: One of 'train', 'val', 'test'.
        batch_size: Batch size.
        num_workers: Number of dataloader workers.

    Returns:
        DataLoader yielding (images, masks) tuples.
    """
    if split not in SPLITS:
        raise ValueError(f"Unknown split '{{split}}'. Available: {{list(SPLITS.keys())}}")

    is_train = (split == "train")
    image_transform = build_train_transforms() if is_train else build_val_transforms()
    mask_transform = build_mask_transform()
    dataset = SegmentationDataset(SPLITS[split], image_transform, mask_transform)
    return DataLoader(dataset, batch_size=batch_size, shuffle=is_train, num_workers=num_workers)
'''

    return [
        ("dataset.py", dataset_code),
        ("transforms.py", transforms_code),
        ("dataloader.py", dataloader_code),
    ]


# ============================================================================
# Route dispatch
# ============================================================================

GENERATORS = {
    ("image", "classification"): _gen_image_classification_dataset,
    ("tabular", "classification"): _gen_tabular_dataset,
    ("tabular", "regression"): _gen_tabular_dataset,
    ("text", "classification"): _gen_text_classification_dataset,
    ("image", "segmentation"): _gen_image_segmentation_dataset,
}


def generate(data_contract: dict, output_dir: str | None = None,
             components: list | None = None) -> dict:
    """Generate data pipeline code from a data contract.

    Args:
        data_contract: Parsed data_contract dict.
        output_dir: Directory to write generated files (None = dry run, return code only).
        components: List of component names to generate (default: all).

    Returns:
        Dict mapping filename to source code.
    """
    data_type = data_contract.get("data_type", "")
    task_type = data_contract.get("task_type", "")

    generator = GENERATORS.get((data_type, task_type))
    if generator is None:
        supported = sorted(f"{d}+{t}" for d, t in GENERATORS)
        raise ValueError(
            f"No generator for data_type='{data_type}' task_type='{task_type}'. "
            f"Supported: {supported}"
        )

    files = generator(data_contract, output_dir or ".")

    if components:
        files = [(name, code) for name, code in files if Path(name).stem in components]

    if output_dir:
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)
        for filename, source in files:
            (out / filename).write_text(source, encoding="utf-8")
            print(f"  Wrote: {out / filename}")

    return {name: code for name, code in files}


def main():
    parser = argparse.ArgumentParser(
        description="Generate Dataset/DataLoader scaffolding from a data contract."
    )
    parser.add_argument("--data-contract", required=True,
                        help="Path to data_contract.yaml")
    parser.add_argument("--inspect", help="Path to inspection result YAML (optional)")
    parser.add_argument("--output-dir", "-o", default=None,
                        help="Output directory for generated code")
    parser.add_argument("--stdout", action="store_true",
                        help="Print generated code to stdout instead of writing files")
    parser.add_argument("--components", default="dataset,transforms,dataloader",
                        help="Comma-separated list of components to generate")
    args = parser.parse_args()

    data_contract = load_yaml(args.data_contract)
    if not data_contract:
        print(f"Error: Could not load data contract from {args.data_contract}", file=sys.stderr)
        sys.exit(1)

    # Optionally merge inspection hints
    if args.inspect:
        inspection = load_yaml(args.inspect)
        if inspection:
            inferred = inspection.get("inferred_format_spec", {})
            if inferred:
                data_contract.setdefault("inferred_format", inferred)

    data_type = data_contract.get("data_type", "")
    task_type = data_contract.get("task_type", "")
    print(f"Generating data pipeline for: {data_type} / {task_type}")
    print(f"  Input:  {data_contract.get('input_spec', {}).get('shape', '?')}")
    print(f"  Output: {data_contract.get('output_spec', {})}")
    print()

    components = [c.strip() for c in args.components.split(",") if c.strip()]
    output_dir = args.output_dir

    if args.stdout:
        output_dir = None

    try:
        files = generate(data_contract, output_dir=output_dir, components=components)
    except ValueError as e:
        print(f"Error: {e}", file=sys.stderr)
        sys.exit(1)

    if args.stdout:
        for name, code in files.items():
            print(f"# ======== {name} ========")
            print(code)
            print()
    elif output_dir:
        print(f"\nDone. Generated {len(files)} file(s) in {output_dir}/")
        print("Review and adjust paths/transforms before training.")
    else:
        print("Dry run (no --output-dir or --stdout). Use --output-dir to write files.")


if __name__ == "__main__":
    main()
