#!/usr/bin/env python3
"""
Generate Dataset/DataLoader scaffolding from a data_contract.yaml.

Usage:
    # Generate data pipeline code from a data contract
    python generate_dataset.py --data-contract data_contract.yaml --output-dir ./datasets

    # Also consume inspection result for richer generation
    python generate_dataset.py --data-contract data_contract.yaml --inspect inspection.yaml --output-dir ./datasets

    # Print generated code to stdout instead of writing files
    python generate_dataset.py --data-contract data_contract.yaml --stdout

    # Only generate specific components
    python generate_dataset.py --data-contract data_contract.yaml --output-dir ./datasets --components dataset,dataloader

Supported routes:
    - image classification (ImageFolder)
    - text classification (JSONL / CSV)
    - tabular classification / regression (CSV / TSV)
    - image segmentation (ImageMaskPairs)
"""

